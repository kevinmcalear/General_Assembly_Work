puts "What day of the week is it? Weekedays only!"
day = gets.chomp.downcase

if day == "monday"
	puts "It's Monday! That sucks for you!"
elsif day == "tuesday"
	puts "It's Tuesday, the week is beginning!"
elsif day == "wednesday"
	puts "It's Wednesday! Hump day!"
elsif day == "thursday"
	puts "home stretch! It's Thursday!"
elsif day == "friday"
	puts "IT'S FRIDAY! THANK JEBUS!"
else
	puts "That's a day I don't understand!"
end
		
		
		
		