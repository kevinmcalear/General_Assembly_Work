puts "What day of the week is it?"
day = gets.chomp

case day
when "Monday"
  puts "Monday, Monday..."
when "Tuesday"
  puts "I don't have an appropriate pop culture reference for Tuesday"
when "Wednesday"
  puts "Guess what day it is?"
  puts "It's hump day!!!!"
when "Thursday"
  puts "The week's almost over"
when "Friday"
  puts "TGIF"
when "Saturday"
  puts "Sportsball!"
when "Sunday"
  puts "Lord's day?"
else
  puts "Error!!!"
end

