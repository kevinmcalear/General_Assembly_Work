puts "Please enter the day of the week:"
dow = gets.chomp.downcase
case dow
when "Monday"
  puts "Better get coffee... and start your week off right."
when "Tuesday"
  puts "Second day in!! At least it's not the first day."
when "Wednesday"
  puts "Hump day!"
when "Thursday"
  puts "The week is Over the Hill!"
when "Friday"
  puts "Wooohoo!!!! PARTAY!"
when "Saturday"
  puts "Day Trip?"
when "Sunday"
  puts "Get to church!"
else
  puts "Study your days of the week... really."
end


