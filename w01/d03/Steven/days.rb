
#Days Of The Week!
puts "What day of the week is it (Monday - Friday) ?"
day = gets.chomp

case day
when "Monday"
  puts "Wooo it's monday!"
when "Tuesday"
  puts "Today is Tuesday! Hooray!"
when "Wednesday"
  puts "Really? Well, ok, it's Wednesday!"
when "Thursday"
  puts "Yay! Thursday is totally awesome"
when "Friday"
  puts "Friiiiidaaaaaay"
end


