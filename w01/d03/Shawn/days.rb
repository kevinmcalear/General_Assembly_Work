puts "Please enter the day of the week..."

day = gets.chomp

if day == 'monday'
    puts "Brace yourself...it's only monday..."

  elsif day == "tuesday"
    puts "It's almost Wednesday!!!"

  elsif day == "wednesday"
    puts "Hooray it's almost the weekend...sort of...."

  elsif day == "thursday"
    puts "just one more day...(after today)..."

  elsif day == "friday"
    puts "Friday, Friday, gotta get up on Friday ... fun ! fun ! fun ! fun!"

  else
    puts "don't bother me it's the weekend..."
  end