    end
  elsif choice == "d"
    delete_grammy = Grammy.delete
    puts delete_grammy
    puts "You've now deleted the entire GRAMMY database. Goodjob."
  else choice != "q"
    "You messed up, please choose another option. Idiot."
  end
end while choice != "q" 
